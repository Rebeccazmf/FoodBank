    private void populateTable(FoodBankEnterprise enterprise) {
        DefaultTableModel model = (DefaultTableModel) tblVolunteerJobList.getModel();

        model.setRowCount(0);

        for (VolunteerJob volunteerJob : enterprise.getVolunteerJobs().getVolunteerJobList()) {
            Object[] row = new Object[2];
            row[0] = volunteerJob.getJobID();
            row[1] = volunteerJob;
            model.addRow(row);
        }
    }

