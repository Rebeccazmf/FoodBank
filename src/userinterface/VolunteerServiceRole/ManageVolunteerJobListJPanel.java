    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int selectedRow = tblVolunteerJobList.getSelectedRow();
        if (selectedRow >= 0) {
            int dialogResult = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete?", "Warning", JOptionPane.YES_NO_OPTION);
            if (dialogResult == JOptionPane.YES_OPTION) {
                VolunteerJob vj = (VolunteerJob) tblVolunteerJobList.getValueAt(selectedRow, 1);
                enterprise.getVolunteerJobs().deleteVolunteerJob(vj);
                JOptionPane.showMessageDialog(this, "The volunteer job has been deleted.", "Info", JOptionPane.INFORMATION_MESSAGE);
                populateTable(enterprise);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please select a row from table", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

