    private void btnViewDtailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewDtailsActionPerformed
        int row = tblVolunteerJobList.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(null, "Pls select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        VolunteerJob volunteerJob = (VolunteerJob) tblVolunteerJobList.getValueAt(row, 1);

        ManageVolunteerJobJPanel manageVolunteerJobJPanel = new ManageVolunteerJobJPanel(userProcessContainer, employeeAccount, organization, enterprise, system, volunteerJob);
        userProcessContainer.add("manageVolunteerJobJPanel", manageVolunteerJobJPanel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_btnViewDtailsActionPerformed

