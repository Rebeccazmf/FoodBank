    public void panelView() {
        if (volunteerJob == null) {
            volunteerJob = new VolunteerJob();
            btnPost.setEnabled(true);
            btnUpdate.setEnabled(false);
            txtJobTitle.setEnabled(true);
            txtAreaJobDescription.setEnabled(true);
            txtRequirement.setEnabled(true);
            displayVolunteerJob();
        } else {
            btnPost.setEnabled(false);
            btnUpdate.setEnabled(true);
            txtJobTitle.setEnabled(false);
            txtAreaJobDescription.setEnabled(false);
            txtRequirement.setEnabled(false);
            displayVolunteerJob();
        }
    }

    public void displayVolunteerJob() {
        lblJobID.setText(String.valueOf(volunteerJob.getJobID()));
        txtJobTitle.setText(volunteerJob.getJobTitle());
        txtAreaJobDescription.setText(volunteerJob.getDescription());
        txtRequirement.setText(volunteerJob.getRequirement());
    }

