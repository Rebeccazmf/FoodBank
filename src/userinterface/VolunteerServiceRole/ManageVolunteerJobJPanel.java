    private void btnPostActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPostActionPerformed
        txtJobTitle.setEnabled(false);
        txtAreaJobDescription.setEnabled(false);
        txtRequirement.setEnabled(false);
        btnPost.setEnabled(false);
        btnUpdate.setEnabled(true);
        String jobTitle = txtJobTitle.getText();
        String description = txtAreaJobDescription.getText();
        String requirement = txtRequirement.getText();

        volunteerJob.setJobTitle(jobTitle);
        volunteerJob.setDescription(description);
        volunteerJob.setRequirement(requirement);
        volunteerJob.setInsitution(enterprise.getName());
        if (!enterprise.getVolunteerJobs().getVolunteerJobList().contains(volunteerJob)) {
            enterprise.getVolunteerJobs().getVolunteerJobList().add(volunteerJob);
        }
        JOptionPane.showMessageDialog(null, "Post volunteer job successfully!");

    }//GEN-LAST:event_btnPostActionPerformed

