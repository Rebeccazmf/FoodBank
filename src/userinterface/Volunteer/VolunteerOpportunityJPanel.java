    private void btnApplyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApplyActionPerformed
        int row = tblVolunteerJob.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row from the table first", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        ApplyVolunteerRequest request = new ApplyVolunteerRequest();
        VolunteerJob job = (VolunteerJob) tblVolunteerJob.getValueAt(row, 2);
        request.setSender(customerAccount);
        request.setJob(job);
        request.setStatus("Apply");
        customerAccount.getWorkQueue().getWorkRequestList().add(request);
        foodbank.getWorkQueue().getWorkRequestList().add(request);
        JOptionPane.showMessageDialog(null, "Successfully applied!", "Information", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btnApplyActionPerformed


