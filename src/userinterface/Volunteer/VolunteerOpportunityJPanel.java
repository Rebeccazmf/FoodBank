    private void populateTable(FoodBankEnterprise enterprise) {
        DefaultTableModel model = (DefaultTableModel) tblVolunteerJob.getModel();

        model.setRowCount(0);

        for (VolunteerJob volunteerJob : enterprise.getVolunteerJobs().getVolunteerJobList()) {
            Object[] row = new Object[3];
            row[0] = volunteerJob.getJobID();
            row[1] = volunteerJob.getInsitution();
            row[2] = volunteerJob;
            model.addRow(row);
        }
    }

