    private void populateHistoryTable(CustomerAccount customerAccount) {
        DefaultTableModel model = (DefaultTableModel) tblVolunteerHistory.getModel();

        model.setRowCount(0);

        for (Map.Entry<Date, VolunteerJob> entry : customerAccount.getVolunteerJobHistory().entrySet()) {
            Object[] row = new Object[3];
            row[0] = entry.getValue().getJobID();
            row[1] = entry.getValue();
            row[2] = entry.getKey();
            model.addRow(row);
        }
    }

    public void populateRequestStatusTable() {
        DefaultTableModel model = (DefaultTableModel) tblApplyStatus.getModel();

        model.setRowCount(0);
        for (WorkRequest request : customerAccount.getWorkQueue().getWorkRequestList()) {
            if (request instanceof ApplyVolunteerRequest) {
                ApplyVolunteerRequest req = (ApplyVolunteerRequest) request;
                Object[] row = new Object[4];
                row[0] = req.getJob().getJobID();
                row[1] = req.getJob();
                row[2] = req.getJob().getInsitution();
                row[3] = req;

                model.addRow(row);
            }
        }
    }

