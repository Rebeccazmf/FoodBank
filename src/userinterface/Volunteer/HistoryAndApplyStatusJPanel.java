    private void btnDoneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDoneActionPerformed
        int row = tblApplyStatus.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row from the table first", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        ApplyVolunteerRequest request = (ApplyVolunteerRequest) tblApplyStatus.getValueAt(row, 3);
        VolunteerJob job = (VolunteerJob) tblApplyStatus.getValueAt(row, 1);
        customerAccount.getVolunteerJobHistory().put(new Date(), job);
        customerAccount.getWorkQueue().getWorkRequestList().remove(request);
        populateRequestStatusTable();
        populateHistoryTable(customerAccount);
    }//GEN-LAST:event_btnDoneActionPerformed


